public class Concat {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 4;
        int num3 = num1 + num2;
        int num4 = num1 * num2;

        System.out.println(num1 + " + " + num2 + " = " + num3 );
        System.out.println(num1 + " * " + num2 + " = " + num4);




    }
}
